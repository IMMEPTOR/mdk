# 1. Сумма кратных 3
n = int(input("N: "))  # Считываем верхнюю границу диапазона и преобразуем ввод в целое число (int)
# Генерируем числа от 1 до N, отбираем те, что делятся на 3 без остатка, и находим их сумму функцией sum()
print("Сумма:", sum(i for i in range(1, n + 1) if i % 3 == 0))

# 2. Инверсия регистра
s = input("Строка: ")  # Получаем текстовую строку от пользователя
# Используем встроенный метод .swapcase(), который меняет большие буквы на маленькие, а маленькие на большие
print("Результат:", s.swapcase())

# 3. Произведение ненулевых
arr = [1, 0, 2, 3, 0, 4]  # Инициализируем исходный список чисел
res = 1  # Создаем переменную для накопления произведения, начиная с 1 (нейтральный множитель)
for x in arr:  # Перебираем каждый элемент (x) в нашем списке
    if x != 0: res *= x  # Если число не равно нулю, умножаем текущий результат на это число
print("Произведение:", res)  # Выводим итоговое произведение всех ненулевых элементов
