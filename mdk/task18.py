# 1. Сумма до нуля
total = 0  # Создаем переменную-аккумулятор для накопления суммы
while True:  # Запускаем бесконечный цикл
    num = int(input("Введи число (0 для стопа): "))  # Считываем число и преобразуем его в целое (int)
    if num == 0: break  # Проверяем условие: если введен ноль, прерываем выполнение цикла (выходим из него)
    total += num  # Если не ноль, прибавляем введенное число к общей сумме
print("Сумма:", total)  # Выводим накопленный результат после завершения цикла

# 2. Самое длинное слово
s = input("Введите предложение: ")  # Получаем от пользователя строку (предложение)
words = s.split()  # Разбиваем строку на список слов (по умолчанию разделитель — пробел)
longest = max(words, key=len)  # Находим слово с максимальной длиной, используя функцию len как критерий сравнения
print("Самое длинное слово:", longest)  # Выводим найденное слово на экран

# 3. Квадрат отрицательных
arr = [-2, 5, -3, 10, -1]  # Задаем исходный список (массив) чисел
# Используем генератор списка с условием: если число x отрицательное, возводим его в квадрат (x**2), иначе оставляем x как есть
res = [x**2 if x < 0 else x for x in arr]
print("Итог:", res)  # Выводим полученный список с преобразованными значениями
