# 1. Делители
n = int(input("N: "))  # Считываем целое число N, для которого будем искать делители
for i in range(1, n + 1):  # Пробегаем циклом все числа от 1 до N включительно
    if n % i == 0: print(i, end=" ")  # Если N делится на i без остатка, выводим i в строку через пробел

# 2. Удаление x перед abc
s = input("\nСтрока: ")  # Считываем строку от пользователя (с переносом строки в начале для порядка)
# Используем метод replace, чтобы найти комбинацию 'xabc' и заменить её на 'abc' (фактически удаляем 'x')
print(s.replace("xabc", "abc"))

# 3. Max отрицательный
arr = [-10, 5, -2, -30, 15]  # Задаем исходный список чисел
neg = [x for x in arr if x < 0]  # С помощью генератора создаем новый список, в котором только отрицательные числа
# Если список neg не пустой, находим в нем максимальное значение (ближайшее к нулю), иначе выводим "Нет"
print("\nMax neg:", max(neg) if neg else "Нет")
