# 1. Кол-во цифр
n = input("Введите число: ")  # Считываем ввод пользователя как строку, чтобы перебрать каждый символ
# Создаем список из символов, которые являются цифрами (isdigit), и выводим его длину (len)
print("Цифр в числе:", len([c for c in n if c.isdigit()]))

# 2. Удаление пунктуации
import string  # Импортируем модуль string, чтобы получить доступ к стандартным наборам символов
s = input("Введите строку: ")  # Получаем от пользователя текстовую строку
# string.punctuation содержит все знаки типа !,.,? и т.д.
# Собираем новую строку, оставляя только те символы c, которых нет в наборе пунктуации
res = "".join(c for c in s if c not in string.punctuation)
print("Результат:", res)  # Выводим очищенную от знаков препинания строку

# 3. Разность сумм индексов
arr = [10, 5, 20, 5, 30]  # Инициализируем исходный массив (список) чисел
even_sum = sum(arr[0::2])  # Считаем сумму элементов на четных индексах (0, 2, 4...) с помощью среза с шагом 2
odd_sum = sum(arr[1::2])  # Считаем сумму элементов на нечетных индексах (1, 3, 5...) со срезом от 1-го индекса
print("Разность:", even_sum - odd_sum)  # Вычисляем разницу между полученными суммами и выводим на экран
