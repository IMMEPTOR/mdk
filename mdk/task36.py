import random  # Подключаем модуль для генерации случайных чисел (используется в 3-м задании)

# 1. Знаки и нули
s = input("Введите строку: ")  # Получаем текстовую строку от пользователя
plus_minus = 0  # Переменная-счетчик для общего количества знаков '+' и '-'
before_zero = 0  # Переменная-счетчик для знаков, за которыми сразу следует символ '0'
for i in range(len(s)):  # Запускаем цикл по всем индексам (номерам позиций) строки
    if s[i] in "+-":  # Проверяем, является ли текущий символ знаком плюс или минус
        plus_minus += 1  # Если да, увеличиваем общий счетчик знаков на 1
        # Если это не последний символ в строке и следующий за ним символ равен '0'
        if i + 1 < len(s) and s[i+1] == '0':
            before_zero += 1  # Увеличиваем счетчик знаков, стоящих перед нулем
print(f"Всего знаков: {plus_minus}, перед нулем: {before_zero}")  # Выводим оба результата через f-строку

# 2. Замена четных символов
s_list = list(input("Введите строку для замены: "))  # Считываем строку и сразу переводим в список, чтобы менять символы
for i in range(1, len(s_list), 2): # Запускаем цикл с шагом 2, начиная с индекса 1 (это четные места: 2-е, 4-е и т.д.)
    if s_list[i] not in "ab": s_list[i] = 'a'  # Если на четном месте не 'a' и не 'b', заменяем символ на 'a'
    else: s_list[i] = 'c'  # Если же там стоит 'a' или 'b', заменяем этот символ на 'c'
print("Итог:", "".join(s_list))  # Собираем список обратно в строку и выводим итоговый результат

# 3. Массив: первый и минимальный
arr = [random.randint(10, 90) for _ in range(15)]  # Генерируем список из 15 случайных целых чисел от 10 до 90
print("До:", arr)  # Выводим исходный массив для наглядности
m_idx = arr.index(min(arr))  # Находим индекс (позицию) самого маленького элемента в массиве
arr[0], arr[m_idx] = arr[m_idx], arr[0]  # Меняем местами самый первый элемент (индекс 0) и минимальный
print("После:", arr)  # Выводим измененный массив, где минимальный элемент теперь стоит на первом месте
