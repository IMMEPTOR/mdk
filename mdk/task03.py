# 1. Квадраты
for i in range(10, 100):  # Перебираем все двузначные числа от 10 до 99
    rev = int(str(i)[::-1])  # Переворачиваем число: превращаем в строку, делаем срез задом наперед и обратно в число
    if ((i + rev)**0.5) % 1 == 0:  # Проверяем, является ли корень из суммы целым числом (остаток от деления на 1 равен 0)
        print(f"Найдено: {i} + {rev} = {i+rev}")  # Если условие верно, выводим число, его перевертыш и их сумму

# 2. Чередование (буква, цифра...)
import random, string  # Импортируем модуль random для выбора и string для доступа к алфавиту
res = ""  # Создаем пустую строку, в которую будем собирать результат
for i in range(1, 11):  # Запускаем цикл от 1 до 10 включительно
    if i % 2 != 0: res += random.choice(string.ascii_letters)  # Если номер итерации нечетный, добавляем случайную букву
    else: res += random.choice("0123456789")  # Если номер итерации четный, добавляем случайную цифру из строки
print("Строка:", res)  # Выводим готовую чередующуюся строку на экран

# 3. Поменять местами 1-й и минимальный
arr = [10, 5, 20, 2, 8]  # Инициализируем исходный список чисел
m_idx = arr.index(min(arr))  # Находим индекс (позицию) минимального элемента в списке
arr[0], arr[m_idx] = arr[m_idx], arr[0]  # С помощью кортежа меняем местами значения по индексу 0 и индексу минимального
print("Массив:", arr)  # Выводим измененный массив на экран