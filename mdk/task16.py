# 1. Сумма четных 1-100
# Используем range с шагом 2, чтобы взять только четные числа (2, 4, 6...), и суммируем их
print("Сумма:", sum(i for i in range(2, 101, 2)))

# 2. Замена a <-> b
s = input("Введите строку: ")  # Получаем от пользователя исходную строку
res = ""  # Создаем пустую строку для накопления измененного текста
for char in s:  # Посимвольно перебираем всю строку в цикле
    if char == 'a': res += 'b'  # Если встретили букву 'a', добавляем в результат 'b'
    elif char == 'b': res += 'a'  # Если встретили букву 'b', добавляем в результат 'a'
    else: res += char  # Все остальные символы добавляем без изменений
print("Итог:", res)  # Выводим полученную строку после всех замен

# 3. Больше среднего
arr = [10, 20, 30, 40, 50]  # Создаем исходный список с числами
avg = sum(arr) / len(arr)  # Вычисляем среднее арифметическое: сумму всех элементов делим на их количество
# Создаем новый список только из тех чисел, что больше среднего, и считаем его длину (количество)
count = len([x for x in arr if x > avg])
print(f"Среднее: {avg}, Кол-во элементов больше среднего: {count}")  # Выводим среднее и итоговый счетчик
