# 1. Четные делители
n = int(input("N: "))  # Запрашиваем у пользователя число N и преобразуем его в целое (int)
for i in range(1, n+1):  # Проходим циклом от 1 до N включительно
    # Проверяем: делится ли N на i без остатка И является ли само число i четным
    if n % i == 0 and i % 2 == 0:
        print(i, end=" ")  # Если оба условия верны, выводим делитель в строку через пробел

# 2. Swap символов
s = list(input("\nСтрока: "))  # Считываем строку и превращаем её в список, чтобы можно было менять элементы
if len(s) > 1:  # Проверяем, что в строке больше одного символа (чтобы было что менять)
    # Меняем местами первый элемент (индекс 0) и последний (индекс -1) через множественное присваивание
    s[0], s[-1] = s[-1], s[0]
print("Результат:", "".join(s))  # Склеиваем список обратно в строку и выводим итог

# 3. Последний индекс X
arr = [1, 2, 3, 2, 4, 5]  # Инициализируем исходный массив чисел
x = int(input("X: "))  # Запрашиваем число X, индекс которого нужно найти
# Запускаем цикл от последнего индекса (длина-1) до нуля с шагом -1 (движемся в обратном порядке)
for i in range(len(arr)-1, -1, -1):
    if arr[i] == x:  # Как только встретили число, равное x
        print("Последний индекс:", i)  # Выводим текущий индекс (он и будет последним в обычном порядке)
        break  # Прерываем цикл, так как мы нашли нужное вхождение с конца
