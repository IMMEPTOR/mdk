# 1. Сумма ряда
n = int(input("n: "))  # Считываем целое число n — количество слагаемых в ряду
# Вычисляем сумму дробей вида 1/i, где i меняется от 1 до n включительно (используем генератор)
s = sum(1/i for i in range(1, n+1))
# Выводим результат, ограничивая точность до 3 знаков после запятой с помощью форматирования .3f
print(f"Сумма: {s:.3f}")

# 2. Строки
s1, s2 = input(), input()  # По очереди считываем две строки от пользователя
diff = abs(len(s1) - len(s2))  # Находим абсолютную разность длин этих строк (модуль разности)
longer = s1 if len(s1) > len(s2) else s2  # Определяем, какая из строк длиннее, и записываем её в переменную
for _ in range(diff): print(longer)  # Выводим более длинную строку столько раз, какова разница в длине

# 3. Поиск вхождений
a, b = [1, 2], [1, 1, 2, 3]  # Задаем два исходных списка (массива) чисел
for x in a:  # Перебираем каждый элемент (x) из первого списка «a»
    # Используем метод .count(x), чтобы узнать, сколько раз число x встречается во втором списке «b»
    print(f"Элемент {x} найден в B {b.count(x)} раз(а)")