# 1. Таблица квадратов
for i in range(1, 11):  # Запускаем цикл, который перебирает целые числа от 1 до 10 включительно
    print(f"{i} | {i*i}")  # Выводим само число и его квадрат, разделенные вертикальной чертой

# 2. Сумма цифр в строке
s = input("Строка: ")  # Получаем от пользователя произвольную строку текста
# Проходим по каждому символу «c» в строке, проверяем, является ли он цифрой, преобразуем в int и суммируем все найденное
print("Сумма:", sum(int(c) for c in s if c.isdigit()))

# 3. Перестановка половин
arr = [1, 2, 3, 4, 5, 6]  # Задаем исходный список (массив) с четным количеством элементов
mid = len(arr) // 2  # Находим индекс середины массива, используя целочисленное деление на 2
# Создаем новый список: берем вторую половину (от mid до конца) и прибавляем к ней первую половину (от начала до mid)
res = arr[mid:] + arr[:mid]
print("Итог:", res)  # Выводим измененный массив, в котором половины поменялись местами
