# 1. Факториал
n = int(input("N: "))  # Считываем целое число N, до которого будем считать произведение
p = 1  # Создаем переменную для накопления произведения и ставим ей 1 (нейтральное значение для умножения)
for i in range(1, n+1): p *= i  # Проходим циклом от 1 до N включительно и умножаем текущее p на каждое i
print("Произведение:", p)  # Выводим итоговый результат (факториал числа N)

# 2. Обрезка/Дописка
s = input("Строка: ")  # Считываем строку от пользователя
if len(s) > 10: print(s[:6])  # Если длина строки больше 10 символов, выводим только первые 6 (срез от 0 до 6)
else: print(s.ljust(12, 'o'))  # В противном случае дополняем строку до 12 символов символом 'o' справа

# 3. Половины
arr = [1, 2, 3, 4, 5, 6]  # Задаем исходный список чисел
mid = len(arr)//2  # Находим индекс середины списка с помощью целочисленного деления
# Считаем сумму первой половины (до mid) и вычитаем из неё сумму второй половины (от mid до конца)
print("Разность:", sum(arr[:mid]) - sum(arr[mid:]))