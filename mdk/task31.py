# 1. Таблица на 7
for i in range(1, 11):  # Запускаем цикл, который перебирает множители от 1 до 10 включительно
    print(f"7 * {i} = {7 * i}")  # Выводим строку с примером и результатом умножения семерки на текущее число i

# 2. Сумма цифр в строке
s = input("Введите строку: ")  # Считываем произвольную строку текста от пользователя
# Перебираем каждый символ в строке, проверяем, является ли он цифрой (isdigit), преобразуем в int и суммируем
res = sum(int(char) for char in s if char.isdigit())
print("Сумма цифр:", res)  # Выводим итоговую сумму всех найденных в строке цифр

# 3. Среднее четных
arr = [2, 5, 8, 10, 3, 4, 7, 12, 1, 6]  # Инициализируем исходный список (массив) из 10 чисел
evens = [x for x in arr if x % 2 == 0]  # Создаем новый список, отфильтровав только четные числа (делятся на 2 без остатка)
if evens:  # Проверяем, не пустой ли список четных чисел (чтобы избежать ошибки деления на ноль)
    # Находим среднее арифметическое: сумму четных чисел делим на их количество
    print("Среднее четных:", sum(evens) / len(evens))
