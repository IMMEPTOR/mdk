# 1. Квадраты
for i in range(10, 21):  # Запускаем цикл, который перебирает целые числа от 10 до 20 включительно
    print(i**2, end=" ")  # Возводим текущее число в квадрат и выводим в одну строку через пробел

# 2. Половины массива
a = [1, 2, 3, 4, 5, 6]  # Инициализируем исходный массив (список) чисел
m = len(a) // 2  # Находим индекс середины массива, используя целочисленное деление на 2
# Выводим новый массив, склеивая вторую половину (от m до конца) с первой половиной (от начала до m)
print("\nНовый массив:", a[m:] + a[:m])

# 3. Замена word
s = input("\nВведите текст: ")  # Запрашиваем у пользователя текстовую строку
# Используем метод .replace(), чтобы найти все вхождения подстроки "word" и заменить их на "letter"
print(s.replace("word", "letter"))
