# 1. Билет
t = input("Номер билета (6 цифр): ")  # Считываем номер билета как строку, чтобы удобно разделить её на части
# t[:3] — берем первые три цифры, t[3:] — последние три. map(int, ...) переводит символы в числа, а sum() их складывает
if sum(map(int, t[:3])) == sum(map(int, t[3:])):
    print("Счастливый")  # Если сумма цифр левой половины равна сумме цифр правой половины
else: 
    print("Нет")  # Если суммы различаются

# 2. Мин в диапазоне
a = [10, 20, 5, 30, 2, 40]  # Инициализируем исходный массив (список) чисел
n = int(input("От (индекс): "))  # Запрашиваем индекс начала диапазона и преобразуем в целое число
k = int(input("До (индекс): "))  # Запрашиваем индекс конца диапазона и преобразуем в целое число
# Берем срез списка от n до k включительно (поэтому k+1) и находим в нем минимальное значение через min()
print("Минимум:", min(a[n : k+1]))
