# 1. Сумма ряда
n = int(input("n: "))  # Считываем количество элементов ряда и преобразуем в целое число (int)
# С помощью генератора создаем последовательность дробей 1/i и сразу складываем их функцией sum()
print("Сумма:", sum(1/i for i in range(1, n+1)))

# 2. Строка 3 раза
s = input("Строка: ")  # Получаем от пользователя произвольную текстовую строку
print(f"{s}, {s}, {s}")  # Выводим введенную строку трижды через запятую, используя f-строку
print("Длина:", len(s))  # Считаем количество символов в строке с помощью функции len() и выводим его

# 3. Сумма max + min
arr = [10, 20, 5, 30, 15]  # Инициализируем исходный массив (список) чисел
# Находим самое маленькое число (min) и самое большое (max) в списке, затем складываем их
print("Сумма min и max:", min(arr) + max(arr))