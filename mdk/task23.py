# 1. Нечетные в диапазоне
n = int(input("N: "))  # Считываем начало диапазона и преобразуем в целое число
m = int(input("M: "))  # Считываем конец диапазона и преобразуем в целое число
for i in range(n, m + 1):  # Проходим циклом от n до m включительно (m + 1)
    if i % 2 != 0: print(i, end=" ")  # Если остаток от деления на 2 не равен нулю, выводим нечетное число в строку

# 2. Слова на 'A'
s = input("\nПредложение: ").upper()  # Считываем строку и переводим в верхний регистр, чтобы считать и 'а', и 'А'
words = s.split()  # Разбиваем предложение на отдельные слова по пробелам и сохраняем в список
# Проходим по списку слов и прибавляем по 1 для каждого слова, которое начинается на букву 'A'
count = sum(1 for w in words if w.startswith('A'))
print("Количество слов на A:", count)  # Выводим итоговое количество найденных слов

# 3. Среднее без краев
arr = [1, 2, 3, 4, 10]  # Исходный массив (минимум 1, максимум 10)
# Сортируем список по возрастанию и берем срез со второго элемента до предпоследнего ([1:-1]), отсекая края
trimmed = sorted(arr)[1:-1] 
# Считаем среднее арифметическое оставшихся чисел: сумму делим на их количество
print("Среднее:", sum(trimmed)/len(trimmed))
