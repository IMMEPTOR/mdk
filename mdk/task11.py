# 1. Таблица умножения
n = int(input("Введите число: "))  # Запрашиваем число у пользователя и преобразуем его в целое (int)
for i in range(1, 11):  # Запускаем цикл от 1 до 10 включительно
    # Выводим строку с примером и результатом умножения через f-строку
    print(f"{n} * {i} = {n * i}")

# 2. Знаки и нули
s = input("Введите строку: ")  # Считываем текстовую строку от пользователя
count_signs = 0  # Переменная для подсчета общего количества знаков '+' и '-'
before_zero = 0  # Переменная для подсчета знаков, за которыми сразу идет ноль
for i in range(len(s)):  # Проходим циклом по всем индексам (позициям) в строке
    if s[i] in "+-":  # Если текущий символ является плюсом или минусом
        count_signs += 1  # Увеличиваем счетчик знаков на 1
        # Проверяем: есть ли следующий символ (чтобы не выйти за границы) и равен ли он нулю
        if i + 1 < len(s) and s[i+1] == '0':
            before_zero += 1  # Если за знаком следует '0', увеличиваем второй счетчик
print(f"Всего знаков: {count_signs}, перед нулем: {before_zero}")  # Выводим итоговые данные

# 3. Сортировка по знаку (без изменения порядка внутри групп)
arr = [10, -5, 3, -2, 0, 7]  # Задаем исходный массив с числами
pos = [x for x in arr if x >= 0]  # Собираем в отдельный список все положительные числа и нули
neg = [x for x in arr if x < 0]  # Собираем в другой список все отрицательные числа
# Склеиваем два списка (сначала идут все положительные, потом все отрицательные) и выводим
print("Результат:", pos + neg)