# 1. Считаем числа
n = int(input("Сколько чисел? "))  # Спрашиваем количество чисел и переводим ввод в целое число (int)
pos, neg, zero = 0, 0, 0  # Создаем три переменные-счетчика и обнуляем их
for i in range(n):  # Запускаем цикл, который сработает столько раз, сколько ввел пользователь
    num = float(input("Введи число: "))  # Считываем само число (тип float для поддержки дробных)
    if num > 0: pos += 1  # Если число > 0, прибавляем единицу к счетчику положительных
    elif num < 0: neg += 1  # Если число < 0, прибавляем единицу к счетчику отрицательных
    else: zero += 1  # Если число равно 0, прибавляем единицу к счетчику нулей
print(f"Плюс: {pos}, Минус: {neg}, Нули: {zero}")  # Выводим результаты через f-строку

# 2. Поиск x и w
s = input("Введи строку: ")  # Получаем от пользователя текстовую строку
x_idx = s.find('x')  # Находим позицию (индекс) первой буквы 'x', если её нет — получим -1
w_idx = s.find('w')  # Находим позицию (индекс) первой буквы 'w', если её нет — получим -1
if x_idx == -1 and w_idx == -1: print("Нет таких букв")  # Если обе буквы отсутствуют (оба индекса -1)
elif x_idx != -1 and (x_idx < w_idx or w_idx == -1): print("Раньше 'x'")  # Если 'x' есть и он левее 'w' (или 'w' нет)
else: print("Раньше 'w'")  # В остальных случаях (если 'w' левее или 'x' вообще не найден)

# 3. Массив
import random  # Подключаем библиотеку для работы со случайными числами
arr = [random.randint(-10, 30) for _ in range(15)]  # Генерируем список из 15 случайных чисел от -10 до 30
print("Массив:", arr)  # Печатаем весь созданный массив для проверки
pos_list = [x for x in arr if x > 0]  # Создаем новый список, отфильтровав только числа больше нуля
if pos_list: print("Среднее:", sum(pos_list)/len(pos_list))  # Если есть хоть одно полож. число, делим их сумму на количество