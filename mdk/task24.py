# 1. Фибоначчи
k = int(input("Сколько чисел? "))  # Запрашиваем количество чисел последовательности и переводим в целое число (int)
a, b = 0, 1  # Инициализируем первые два числа последовательности Фибоначчи (0 и 1)
for _ in range(k):  # Запускаем цикл, который выполнится ровно k раз
    print(a, end=" ")  # Выводим текущее число в строку через пробел
    a, b = b, a + b  # Одновременно обновляем переменные: новой «a» становится «b», а новой «b» — их сумма

# 2. Скрытие цифр
s = input("\nСтрока: ")  # Считываем текстовую строку от пользователя (с переносом строки в начале)
# Перебираем каждый символ c в строке: если это цифра (isdigit), заменяем на '*', иначе оставляем как есть
res = "".join('*' if c.isdigit() else c for c in s)
print(res)  # Выводим полученную строку, где все цифры скрыты звездочками

# 3. Проверка порядка
arr = [1, 3, 5, 10]  # Задаем исходный список (массив) чисел
# С помощью функции all проверяем условие для всех пар: текущий элемент (arr[i]) должен быть меньше или равен следующему (arr[i+1])
is_sorted = all(arr[i] <= arr[i+1] for i in range(len(arr)-1))
print("По возрастанию?", is_sorted)  # Выводим результат проверки (True, если массив отсортирован, иначе False)
