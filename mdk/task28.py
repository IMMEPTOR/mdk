# 1. Сумма квадратов
n = int(input("N: "))  # Считываем целое число N — верхнюю границу диапазона
# С помощью генератора возводим каждое число от 1 до N в квадрат и находим их общую сумму функцией sum()
print("Сумма квадратов:", sum(i**2 for i in range(1, n+1)))

# 2. Найти цифры
s = input("Строка: ")  # Получаем от пользователя произвольную строку
digits = [c for c in s if c.isdigit()]  # Выбираем из строки только те символы, которые являются цифрами
print("Цифры в строке:", "".join(digits))  # Соединяем список найденных цифр в одну строку и выводим на экран

# 3. Есть ли отрицательные
arr = [5, 10, -1, 3]  # Задаем исходный список (массив) чисел
# Используем функцию any(), которая возвращает True, если хотя бы для одного элемента x выполняется условие x < 0
has_neg = any(x < 0 for x in arr)
# Выводим результат: если has_neg равен True, печатаем "Да", в противном случае — "Нет"
print("Есть отрицательные?", "Да" if has_neg else "Нет")
