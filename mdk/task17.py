# 1. Делится на 3 и 5
n = int(input("N: "))  # Считываем верхнюю границу диапазона и переводим в целое число (int)
for i in range(1, n + 1):  # Проходим циклом от 1 до N включительно
    # Если число делится на 15 (это одновременно делимость на 3 и на 5), выводим его
    if i % 15 == 0: print(i, end=" ")

# 2. Строка
s = input("\nСтрока: ")  # Получаем строку от пользователя (с переносом строки для красоты)
if len(s) > 10:  # Проверяем длину: если в строке больше 10 символов
    print(s.replace(" ", ""))  # Используем replace, чтобы найти все пробелы и заменить их на «ничего» (удалить)
else:  # Если длина строки 10 символов или меньше
    print("#" + s)  # Добавляем символ решетки в начало строки

# 3. Второй максимум
arr = [10, 45, 2, 30, 45, 12]  # Задаем исходный массив чисел
# 1. set(arr) убирает дубликаты. 2. list() превращает обратно в список. 3. sorted(..., reverse=True) сортирует по убыванию
unique_sorted = sorted(list(set(arr)), reverse=True)
# Если в списке осталось больше одного элемента, берем тот, что под индексом 1 (второй по величине)
print("\nВторой максимум:", unique_sorted[1] if len(unique_sorted) > 1 else "Нет")
