# 1. Нечетные делители
n = int(input("N: "))  # Запрашиваем у пользователя число N и преобразуем его в целое (int)
for i in range(1, n + 1):  # Запускаем цикл от 1 до N включительно, чтобы проверить каждое число
    # Проверяем два условия: i является делителем N (остаток 0) И i — нечетное (остаток от деления на 2 не равен 0)
    if n % i == 0 and i % 2 != 0:
        print(i, end=" ")  # Если оба условия верны, выводим делитель в одну строку через пробел

# 2. Гласные
s = input("\nСтрока: ").lower()  # Считываем строку и сразу переводим её в нижний регистр для удобства сравнения
vowels = "aeiouy"  # Создаем строку-эталон, содержащую все английские гласные буквы
# С помощью генератора перебираем символы строки s и считаем (sum) единицу для каждого символа, который есть в vowels
count = sum(1 for char in s if char in vowels)
print("Гласных:", count)  # Выводим общее количество найденных гласных букв

# 3. Нули в конец
arr = [0, 1, 0, 3, 12, 0, 5]  # Задаем исходный массив с числами, включая нули
non_zeros = [x for x in arr if x != 0]  # Собираем в новый список все элементы, которые НЕ равны нулю
zeros = [x for x in arr if x == 0]  # Собираем в отдельный список только нулевые элементы
# Склеиваем списки: сначала идут все ненулевые числа, а затем все нули в конец
print("Результат:", non_zeros + zeros)
