# 1. Факториал
n = int(input("N: "))  # Запрашиваем у пользователя число N и преобразуем его в целый тип (int)
res = 1  # Создаем переменную для результата и приравниваем к 1 (база для умножения)
for i in range(1, n + 1): res *= i  # Проходим циклом от 1 до N включительно, последовательно умножая числа
print("Результат:", res)  # Выводим итоговое значение факториала на экран

# 2. Замена
s = input("Строка: ")  # Считываем текстовую строку от пользователя
# Используем встроенный метод .replace(), чтобы найти все подстроки "word" и заменить их на "letter"
print(s.replace("word", "letter"))

# 3. Общие элементы
a = [1, 2, 3, 4]  # Создаем первый список (массив) чисел
b = [3, 4, 5, 6]  # Создаем второй список (массив) чисел
# Превращаем списки в множества (set), находим их пересечение через оператор & и переводим обратно в список
print("Общие:", list(set(a) & set(b)))