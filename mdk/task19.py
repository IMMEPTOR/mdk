# 1. Сумма цифр
n = input("Введите число: ")  # Считываем ввод пользователя как строку, чтобы было удобно перебирать символы
# Проходим циклом по каждому символу d, проверяем, является ли он цифрой (isdigit), переводим в int и суммируем
print("Сумма цифр:", sum(int(d) for d in n if d.isdigit()))

# 2. Палиндром
# Считываем строку, переводим в нижний регистр (.lower) и удаляем все пробелы (.replace), чтобы игнорировать их
s = input("Введите строку: ").lower().replace(" ", "")
# Сравниваем строку с её перевернутой копией (срез [::-1]). Если они равны — это палиндром
if s == s[::-1]: print("Палиндром")
else: print("Нет")

# 3. Сумма между min и max
arr = [10, 1, 5, 8, 2, 20, 3]  # Задаем исходный список чисел
i_min, i_max = arr.index(min(arr)), arr.index(max(arr))  # Находим индексы (позиции) минимального и максимального элементов
# Определяем, какой из индексов идет раньше, а какой позже, чтобы правильно сделать срез
start, end = min(i_min, i_max), max(i_min, i_max)
# Берем срез элементов, находящихся строго МЕЖДУ ними (start+1), и вычисляем их сумму
print("Сумма между ними:", sum(arr[start+1 : end]))
