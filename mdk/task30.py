# 1. Нечетные цифры
n = input("Число: ")  # Считываем ввод пользователя как строку, чтобы перебирать отдельные цифры
# Перебираем символы d, проверяем, что это цифра и что она нечетная, превращаем в int и суммируем
res = sum(int(d) for d in n if d.isdigit() and int(d) % 2 != 0)
print("Сумма нечетных цифр:", res)  # Выводим полученную сумму всех нечетных цифр

# 2. Переворот слов
s = input("Строка: ")  # Получаем от пользователя предложение (строку)
words = s.split()  # Разбиваем строку на список слов, используя пробел как разделитель
# Для каждого слова w в списке создаем его перевернутую копию с помощью среза [::-1]
res = [w[::-1] for w in words]
print("Результат:", " ".join(res))  # Соединяем перевернутые слова обратно в строку через пробел

# 3. Фильтр по диапазону
arr = [5, 15, 25, 60, 10]  # Задаем исходный список (массив) чисел
# С помощью генератора выбираем только те числа x, которые строго больше 10 и строго меньше 50, и суммируем их
res = sum(x for x in arr if 10 < x < 50)
print("Сумма (10 < x < 50):", res)  # Выводим итоговую сумму отфильтрованных элементов
