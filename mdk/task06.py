import random, string  # Импортируем модули для генерации случайных значений и работы со строками

# 1. Числа
print("Числа:")  # Выводим заголовок для первой части задания
for i in range(20, 51):  # Запускаем цикл, перебирающий числа в диапазоне от 20 до 50 включительно
    if i % 3 == 0 and i % 5 != 0:  # Проверяем условие: число делится на 3 БЕЗ остатка И на 5 С остатком
        print(i, end=" ")  # Если условие верно, выводим число в одну строку через пробел

# 2. Группы по 3 символа
s = "abcdefghi"  # Задаем исходную строку
groups = []  # Создаем пустой список для хранения измененных групп символов
for i in range(0, len(s) - 2, 3):  # Идем по строке с шагом 3, чтобы брать тройки символов
    part = list(s[i:i+3]) # Извлекаем срез из 3-х символов и преобразуем его в список (чтобы можно было менять буквы)
    part[1] = random.choice(string.ascii_letters) # Выбираем случайную букву и заменяем ею средний (второй) элемент в списке
    groups.append("".join(part)) # Склеиваем список обратно в строку и добавляем в итоговый список groups
groups.sort() # Сортируем полученный список групп по алфавиту
print("\nГруппы:", groups) # Выводим результат (добавив перенос строки в начале для красоты)

# 3. Swap Max/Min
arr = [10, 50, 2, 30, 15] # Инициализируем массив чисел
idx_min = arr.index(min(arr)) # Находим индекс (позицию) самого маленького числа в списке
idx_max = arr.index(max(arr)) # Находим индекс (позицию) самого большого числа в списке
arr[idx_min], arr[idx_max] = arr[idx_max], arr[idx_min] # Меняем значения местами, используя множественное присваивание
print("Массив:", arr) # Выводим измененный массив на экран