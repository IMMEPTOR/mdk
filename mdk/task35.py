# 1. Произведение цифр
n = input("Число: ")  # Считываем ввод пользователя как строку, чтобы перебрать каждую цифру отдельно
res = 1  # Создаем переменную для накопления произведения и ставим 1 (нейтральное число для умножения)
for d in n:  # Проходим циклом по каждому символу в строке n
    if d.isdigit(): res *= int(d)  # Если символ является цифрой, преобразуем его в число и умножаем текущий результат
print("Произведение:", res)  # Выводим итоговое произведение всех найденных цифр

# 2. Края строки
s = input("Строка: ")  # Получаем от пользователя произвольную текстовую строку
# Проверяем, что строка не пустая (длина > 0) И что первый символ (индекс 0) совпадает с последним (индекс -1)
if len(s) > 0 and s[0] == s[-1]: print("Да")
else: print("Нет")  # Если строка пуста или первый и последний символы разные

# 3. Четные на нечетных местах
arr = [10, 2, 5, 4, 3, 6] # Создаем исходный массив чисел
# Примечание: "нечетное место" (1-е, 3-е, 5-е) в программировании соответствует четным индексам (0, 2, 4...)
count = 0  # Обнуляем счетчик для поиска подходящих чисел
for i in range(0, len(arr), 2):  # Запускаем цикл от 0 до конца списка с шагом 2 (только нечетные места)
    if arr[i] % 2 == 0:  # Проверяем, является ли само число по этому индексу четным (делится на 2 без остатка)
        count += 1  # Если число четное, увеличиваем счетчик на 1
print("Результат:", count)  # Выводим итоговое количество четных чисел, стоящих на нечетных позициях
