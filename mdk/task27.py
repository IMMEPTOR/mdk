# 1. Числа на 7
# Создаем список чисел от 1 до 100, у которых остаток от деления на 10 равен 7 (то есть оканчиваются на 7)
print("Числа:", [i for i in range(1, 101) if i % 10 == 7])

# 2. Замена или дописка
s = input("Строка: ")  # Считываем текстовую строку от пользователя
if '!' in s:  # Проверяем, содержится ли в строке хотя бы один восклицательный знак
    print(s.replace('!', '.'))  # Если есть, заменяем все восклицательные знаки на точки и выводим
else:  # Если восклицательных знаков в строке не оказалось
    print(s + '.')  # Просто приписываем точку в самый конец строки

# 3. Размах (Max - Min)
arr = [10, 2, 30, 1, 5]  # Задаем исходный список (массив) чисел
# Находим самое большое число через max(), самое маленькое через min() и вычисляем разность между ними
print("Разность:", max(arr) - min(arr))
